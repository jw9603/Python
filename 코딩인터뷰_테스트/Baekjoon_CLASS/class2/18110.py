##################################### 문제 이해 #####################################
# 어떤 문제의 난이도는 그 문제를 푼 사람들이 제출한 난이도 의견을 바탕으로 결정한다.
# 난이도 의견은 그 사용자가 생각한 난이도를 의미하는 정수 하나로 주어진다.
# solved.ac가 사용자들의 의견을 바탕으로 나이도를 결정하는 방식은 다음과 같다.

# 1. 아직 아무 의견이 없다면 문제의 난이도는 0으로 결정
# 2. 의견이 하나 이상 있다면, 문제의 난이도는 모든 사람의 난이도 의견의 30% 절사 평균으로 결정

# 30% 절사평균의 경우 위에서 15%, 아래에서 15%를 각각 제외하고 평균을 계산한다. 
# 제외되는 사람의 수는 위, 아래에서 각각 반올림한다.
# 마지막으로, 계산된 평균도 정수로 반올림된다.
# 사용자들이 어떤 문제에 제출한 난이도 의견 목록이 주어질 때, solved.ac가 결정한
# 문제의 난이도를 계산하는 프로그램을 작성하시오.
# 입력:
# 첫 째 줄에 난이도 의견의 개수 n이 주어진다.
# 둘 째 줄에 n개의 난이도 의견이 한 줄에 하나씩 주어짐.
# 출력: solved.ac가 결정한 문제의 난이도를 출력한다.
# 알고리즘:
# 1. 의견의 개수 n을 입력받는다.
# 2. 의견을 리스트로 입력받는다.
# 3. 의견이 없다면 0을 출력한다.
# 4. 의견이 있다면, 의견을 정렬한다.
# 5. 위에서 15%, 아래에서 15%를 제외한다.
# 6. 남은 의견의 평균을 구한다.
# 7. 평균을 반올림하여 출력한다.
##################################### 문제 이해 #####################################
import sys
def round_half_up(value):
    return int(value) + 1 if value - int(value) >= 0.5 else int(value)

def main():
    n = int(sys.stdin.readline().strip())

    if n == 0:
        print(0)
        return
    
    opinions = [int(sys.stdin.readline().strip()) for _ in range(n)]
    opinions.sort()

    cut = round_half_up(n * 0.15)
    trimmed = opinions[cut:n - cut]

    avg = round_half_up(sum(trimmed) / len(trimmed))
    print(avg)

if __name__ == "__main__":
    main()  